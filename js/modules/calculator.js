// ADD FOUR PUBLIC FUNCTIONS BELOW

// ADD FUNCTION
const add = (x, y) => {
    return x + y;
};

// SUBTRACT FUNCTION
const sub = (x, y) => {
    return x - y;
};

// MULTIPLY FUNCTION
const multiply = (x, y) => {
    return x * y;
};

// DIVIDE FUNCTION
const divide = (x, y) => {
    return x / y;
};

// EXPORT THE FOUR PUBLIC FUNCTIONS
export {add};
export {sub};
export {multiply};
export {divide};